#include <stdlib.h>
#include <stdio.h>
#include <string.h>

int	ft_str_length(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	int		ix;
	char	*dest;

	ix = 0;
	dest = (char *)malloc(ft_str_length(src) * (sizeof(char) + 1));
	if (!dest)
		return (0);
	while (src[ix])
	{
		dest[ix] = src[ix];
		ix++;
	}
	dest[ix] = '\0';
	return (dest);
}
