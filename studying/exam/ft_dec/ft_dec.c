#include <stdio.h>

int	ft_dec(int *ptr)
{
	*ptr -= 1;
	return (*ptr);
}

int	main(void)
{
	int	*ptr;

	*ptr = 5;
	printf("%d", ft_dec(ptr));
	return (0);
}
